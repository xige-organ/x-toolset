const validateReg = {
  /* 只支持中文 */
  chineseReg: /[\u4e00-\u9fa5]+/,

  /* 只支持中英文 */
  ceReg: /[^\u4E00-\u9FA5a-zA-Z]/g,

  /* 中英文数字 */
  cenReg: /[^\u4E00-\u9FA5a-zA-Z0-9]/g,

  /* 匹配特殊字符 */
  specialCharReg: /[`~!@#$^&*()=|{}':;',\\\[\]\.<>\/?~！@#￥……&*（）——|{}【】'；：""'。，、？」「』『\]\[\+\%\+]/g,

  /* 手机号 */
  mobileReg: /^(?:(?:\+|00)86)?1(?:(?:3[\d])|(?:4[5-79])|(?:5[0-35-9])|(?:6[5-7])|(?:7[0-8])|(?:8[\d])|(?:9[1589]))\d{8}$/,

  /* 大陆身份证 */
  lscReg: /^\d{6}((((((19|20)\d{2})(0[13-9]|1[012])(0[1-9]|[12]\d|30))|(((19|20)\d{2})(0[13578]|1[02])31)|((19|20)\d{2})02(0[1-9]|1\d|2[0-8])|((((19|20)([13579][26]|[2468][048]|0[48]))|(2000))0229))\d{3})|((((\d{2})(0[13-9]|1[012])(0[1-9]|[12]\d|30))|((\d{2})(0[13578]|1[02])31)|((\d{2})02(0[1-9]|1\d|2[0-8]))|(([13579][26]|[2468][048]|0[048])0229))\d{2}))(\d|X|x)$/,

  /* 澳门身份证 */
  OLscReg: /^[1|5|7]\d{6}\(\d\)$/,

  /* 香港身份证 */
  HLscReg: /^[a-zA-Z]\d{6}\([\dA]\)$/,

  /* 台湾身份证 */
  TLscReg: /^[a-zA-Z][0-9]{9}$/,

  /* 护照【包含港澳】 */
  passportReg: /(^[EeKkGgDdSsPpHh]\d{8}$)|(^(([Ee][a-fA-F])|([DdSsPp][Ee])|([Kk][Jj])|([Mm][Aa])|(1[45]))\d{7}$)/,

  /* 密码【最少6为，包含至少1个大写字母，1个小写字母，1个数字，1个特殊字符】 */
  passReg: /^\S*(?=\S{6,})(?=\S*\d)(?=\S*[A-Z])(?=\S*[a-z])(?=\S*[!@#$%^&*? ])\S*$/,
  
  /* 密码【大写字母，小写字母，数字，特殊字符，‘@#$%^&*~()-+=’中任意3项密码】 */
  passReg: /^(?![a-zA-Z]+$)(?![A-Z0-9]+$)(?![A-Z\W_!@#$%^&*`~()-+=]+$)(?![a-z0-9]+$)(?![a-z\W_!@#$%^&*`~()-+=]+$)(?![0-9\W_!@#$%^&*`~()-+=]+$)[a-zA-Z0-9\W_!@#$%^&*`~()-+=]/,

  /* 邮箱 */
  emailReg: /^[A-Za-z0-9\u4e00-\u9fa5]+@[a-zA-Z0-9_-]+(\.[a-zA-Z0-9_-]+)+$/
}
export default validateReg